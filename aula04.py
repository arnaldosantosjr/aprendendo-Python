# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número inteiro
# positivo ou negativo. int sem sinal é positivo

print(11) # int
print(-11) #int
print(0) # int

# float -> Número com pontos flutuantes
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considearado positivo

print(1.1,  10.11) #float
print(0.0, -0.1) #float 


# A função type mostra o tipo que o python
#inferiu ao valor.

print(     type('Arnaldo')     )
print(     type('Arnaldo')     )
print(     type(1.1), type(-1.1), type(0.0)     )